// Enum to represent different HTTP request types.
enum HttpRequestType {
  POST,   // Represents a POST request.
  GET,    // Represents a GET request.
  PUT,    // Represents a PUT request.
  PATCH,  // Represents a PATCH request.
}

// Enum to represent different HTTP response types.
enum HttpResponseType {
  XML,   // Represents an XML response.
  REST,  // Represents a standard REST/JSON response.
}
